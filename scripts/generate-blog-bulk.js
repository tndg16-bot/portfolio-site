// 記事を生成して保存
function generateAndSaveArticles(startNum, endNum) {
  console.log(`Generating articles from ${startNum} to ${endNum}...`);
  
  if (!fs.existsSync(blogDir)) {
    fs.mkdirSync(blogDir, { recursive: true });
  }
  
  const topics = generateMoreTopics(startNum, endNum);
  let created = 0;
  
  for (const topic of topics) {
    const content = generateArticleContent(topic);
    const fileName = `${topic.slug}.md`;
    const filePath = path.join(blogDir, fileName);
    
    fs.writeFileSync(filePath, content, 'utf8');
    created++;
    
    if (created % 100 === 0) {
      console.log(`Created ${created} articles...`);
    }
  }
  
  console.log(`\nCompleted! Created ${created} articles.`);
  return created;
}

